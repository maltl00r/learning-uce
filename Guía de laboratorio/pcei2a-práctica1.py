# Estructura de Datos
# Ejercicio Práctico: Gestión de Inventarios
#
# Los estudiantes deben crear un programa utilizando el IDE Visual Studio Code
# que realice las siguientes tareas:
#
# 1. Solicitar ingreso de productos:
#    El programa debe pedir al usuario que ingrese los detalles de al menos
#    tres productos de electrónica (por ejemplo: "Portátil", "Smartphone", "Auriculares").
#
# 2. Detalles requeridos por producto:
#    Para cada producto, el usuario debe ingresar:
#    - Modelo (string)
#    - Precio (float)
#    - Stock (integer)
#
# 3. Implementación de estructuras de datos:
#    El programa debe utilizar las siguientes estructuras:
#
#    - Diccionario principal (inventario):
#      Se utilizará para almacenar todos los productos.
#      * Clave: nombre del producto (string, ej: "Portátil")
#      * Valor: una lista que contendrá los detalles del producto
#
#    - Tupla para atributos:
#      Dentro de la lista (valor del diccionario), se debe usar una tupla
#      para representar los atributos específicos de un producto,
#      asegurando que los datos no sean modificables una vez asignados.
#      * La tupla debe contener: (modelo, precio, stock)
#
# 4. Impresión de resultados:
#    El programa debe imprimir en pantalla el diccionario completo
#    (inventario) de manera clara y legible.
#
# 5. Evaluación:
#    El ejercicio será evaluado en clase mediante la rúbrica
#    de evaluación (ver Anexo 1).